package task6;

public class Animal {
    String name;
    String species;
    String color;
    int age;
    float weight;
    String originPlace;
    String feedingType;

    public Animal(String name, String species, String color, int age, float weight, String originPlace, String feedingType){
        this.name = name;
        this.species = species;
        this.color = color;
        this.age = age;
        this.weight = weight;
        this.originPlace = originPlace;
        this.feedingType = feedingType;
    }




}
